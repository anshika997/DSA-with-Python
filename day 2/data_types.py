# Data types in Python

age = 19 # integer
height = 5.2 # float
name = "Anshika" # string
is_student = True # boolean value
c= 1+2j # complex number
print("Age:",type(age), age)
print("Height:",type(height), height)
print("Name:",type(name), name)
print("Is Student:",type(is_student), is_student)
print("Complex Number:",type(c), c)
languages = None # NoneType
print("Languages:",type(languages), languages)