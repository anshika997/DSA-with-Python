# Variable Naming Rules in Python:

# 1. Variable name must start with a letter (a–z / A–Z) or underscore (_)

# 2. It cannot start with a number(0,1,2,3...)

# 3. It can contain only letters, numbers, and underscores(no other special characters)

# 4. Spaces are not allowed in variable names

# 5. Variable names are case-sensitive (age and Age are different)

# 6. Python keywords( predefined words ) cannot be used as variable names

# 7. Use meaningful and descriptive variable names

# 8. Follow snake_case(Words are written in lowercase) naming convention in Python


