# arithmetic operators : + , - , * , / , % , // , **  


print( 35 + 57 )  # addition
print( 57 - 35 )  # subtraction
print( 5 * 7 )    # multiplication
print( 35 / 7 )   # division
print( 35 // 7 )   # floor division
print( 5 ** 3 )   # exponentiation
print( 35 % 6 )   # modulus(remainder) 

